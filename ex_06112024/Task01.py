# = operator: This is the assignment operator. It is used to assign a value to a variable.
# x = 4
# Here the x value is 4
x=4
print(x)

# == operator: This is the equality operator. It is used to compare two values to check if they are equal
a=5
b=5
print(a==b) #Here a and b are equal so it will print True here we compare a and b

# ** operator: This is the exponentiation operator. It is used to raise a number to a power.
a=5
b=2
print(a**b) #Here a is raised to the power of b and the result is 25
# ^ operator: This is the bitwise XOR operator. It is used to perform a bitwise exclusive OR operation on two numbers.
a=6 # 6 = binary code 0000 0110
b=3 # 3 = Binary code 0000 0011
print(a^b) #Here a and b are bitwise XORed and the result is 3 (0000 0011)

